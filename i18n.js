let currentLang = localStorage.getItem('lang') || 'fr';

function t(key) {
  return (TRANSLATIONS[currentLang] || TRANSLATIONS.fr)[key] || TRANSLATIONS.fr[key] || key;
}

function applyTranslations() {
  const lang = currentLang;

  // Font switch for Tamil
  document.body.style.fontFamily = lang === 'ta'
    ? "'Noto Sans Tamil', 'Jost', sans-serif"
    : "'Jost', sans-serif";
  document.documentElement.lang = lang === 'ta' ? 'ta' : lang;

  // data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== undefined) el.innerHTML = val;
  });

  // data-i18n-placeholder
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const val = t(key);
    if (val !== undefined) el.placeholder = val;
  });

  // data-i18n-title (for buttons / tooltips)
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key);
    if (val !== undefined) el.title = val;
  });

  // Update active lang buttons everywhere
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const l = btn.getAttribute('data-lang');
    btn.classList.toggle('active', l === lang);
  });

  // Update page title if present
  const titleEl = document.querySelector('[data-page-title]');
  if (titleEl) {
    const key = titleEl.getAttribute('data-page-title');
    const val = t(key);
    if (val) document.title = val + ' | Lakshwini DANASSEGARANE';
  }
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  applyTranslations();
  // close dropdown if open
  const dd = document.getElementById('langDropdown');
  if (dd) dd.classList.remove('open');
}

// Init on load — apply translations + sync the flag/label UI
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  // If the page has a lang dropdown (project pages), sync its visual state
  if (typeof updateLangUI === 'function') {
    updateLangUI(currentLang);
  }
});
